
age = int(input("This program calculates whether you qualify to do these specific actions. Please enter your age to begin."))
if age >= 18:
 print("You qualify to vote!")
else:
 print("You do not qualify to vote")
if age >= 16:
 print("You qualify to drive!")
else:
 print("You do not qualify to drive.")
if age >= 15:
 print("You qualify to get a learner's permit!")
else:
 print("You do not qualify to get a learner's permit")
if age >= 5:
 print("You qualify to go to school!")
else:
 print("You do not qualify to go to school")